if [ ! -f "analyze_to_file.sh" ]; then
    echo "오류: analyze_to_file.sh 스크립트가 현재 디렉터리에 없습니다."
    exit 1
fi

echo "=========================================================="
echo "          전체 데이터 폴더 분석 시작"
echo "=========================================================="

# '0731'을 제외하고 '08'로 시작하는 모든 폴더를 순회
for DIR in 08*; do
    if [ -d "$DIR" ] && [ "$DIR" != "0731" ]; then
        echo "--> [INFO] $DIR 폴더 분석 시작..."
        
        # 해당 폴더로 이동
        cd "$DIR" || continue
        
        # 분석 스크립트를 상위 폴더에서 실행 (결과는 현재 폴더에 저장)
        ../analyze_to_file.sh "${DIR}_analysis.txt"
        
        # 상위 폴더로 다시 이동
        cd ..
        
        echo "--> [DONE] $DIR 분석 완료. 결과: ${DIR}/${DIR}_analysis.txt"
        echo "---------------------------------------------------------"
    fi
done

echo "=========================================================="
echo "          모든 폴더 분석 작업 완료"
echo "=========================================================="
